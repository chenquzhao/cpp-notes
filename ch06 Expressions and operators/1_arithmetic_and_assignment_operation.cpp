#include <stdio.h>

int main()
{	
	// basic math calculation
	3 + 4; // 7
	3 - 4; // -1
	3 * 4; // 12
	0.2 + 0.4; // 0.6
	0.2 - 0.4; // -0.2
	0.2 * 0.4; // 0.08

	// module (integer only) & division
	22 / 5; // 4
	22 % 5; // 2
	22.0 / 5.0; // 4.4

	// assigning operator: '='
	int a = 10; // left value -> storage, right value -> value
	int b = 2;
	a += b; // a = a + b
	printf("a: %d", a = 100);

	return 0;
}
